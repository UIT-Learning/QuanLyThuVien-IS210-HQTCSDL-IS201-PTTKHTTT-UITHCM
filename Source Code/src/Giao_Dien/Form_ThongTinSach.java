    private void jButtonXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonXacNhanActionPerformed
        KetNoiCSDL KN = new KetNoiCSDL();
        Sach S = new Sach();
//      Man_Hinh_Chinh MHC = new Man_Hinh_Chinh();
        S.setMaSach(jTextMaSach.getText());
        S.setTenSach(jTextTenSach.getText());
        S.setTheLoai(jTextTheLoai.getText());
        
        S.setSoTrang((int) jSpinnerSoTrang.getValue());
        S.setTacGia(jTextTacGia.getText());
        S.setNXB(jTextNXB.getText());
        S.setViTri(jTextViTri.getText());
        S.setSoLuong((int) jSpinnerSoLuong.getValue());
        
        if(Select_Sach == null){
            try{
//                String URL;
//                URL = "jdbc:oracle:thin:@localhost:1522:NQL";
//                String username = "QUOCLUU";
//                String password = "abc";
                Connection conn = DriverManager.getConnection(KN.URL ,KN.username, KN.password);
                String sql = "INSERT INTO SACH (MaSach, TenSach, TheLoai, SoTrang, TacGia, NXB, ViTri, SoLuong) \n" +
                                        "VALUES (?,?,?,?,?,?,?,?)";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, S.getMaSach());
                ps.setString(2, S.getTenSach());
                ps.setString(3, S.getTheLoai());
                ps.setInt(4, S.getSoTrang());
                ps.setString(5, S.getTacGia());
                ps.setString(6, S.getNXB());
                ps.setString(7, S.getViTri());
                ps.setInt(8, S.getSoLuong());
                ResultSet rs = ps.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Thêm sách thành công!");
                    ResetSach();
//                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Thêm sách thất bại!");
                }
                ps.close();
                conn.close();
            }catch(HeadlessException | SQLException ex){
                System.out.println(ex);
            }
        }
        else{
            try{
//                String URL;
//                URL = "jdbc:oracle:thin:@localhost:1522:NQL";
//                String username = "QUOCLUU";
//                String password = "abc";
                Connection conn = DriverManager.getConnection(KN.URL ,KN.username, KN.password);
                String sql = "UPDATE SACH SET TenSach = ?, TheLoai = ?," +
                                            "SoTrang = ?,TacGia = ?,NXB = ?," +
                                            "ViTri = ?, SoLuong = ?" +
                                    "WHERE MaSach = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(8, Select_Sach);
                
                ps.setString(1, S.getTenSach());
                ps.setString(2, S.getTheLoai());
                ps.setInt(3, S.getSoTrang());
                ps.setString(4, S.getTacGia());
                ps.setString(5, S.getNXB());
                ps.setString(6, S.getViTri());
                ps.setInt(7, S.getSoLuong());
                
                ResultSet rs = ps.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Cập nhật sách thành công!");
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Cập nhật sách thất bại!");
                }
                ps.close();
                conn.close();
            }catch(HeadlessException | SQLException ex){
                JOptionPane.showMessageDialog(this,ex);
            }
        }

    }//GEN-LAST:event_jButtonXacNhanActionPerformed

