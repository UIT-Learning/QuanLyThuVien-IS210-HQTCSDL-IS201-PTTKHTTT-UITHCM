    private void jButtonChinhSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChinhSuaActionPerformed
        KetNoiCSDL KN = new KetNoiCSDL();
        if(Select_Sach != null){
            try{
//                String URL;
//                URL = "jdbc:oracle:thin:@localhost:1522:NQL";
//                String username = "QUOCLUU";
//                String password = "abc";
                Connection conn = DriverManager.getConnection(KN.URL ,KN.username, KN.password);
                String sql = "SELECT * FROM Sach\n" +
                             "WHERE MaSach=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, Select_Sach);
                ResultSet rs = ps.executeQuery();
                if(rs.next()){
                    jTextMaSach.setText(rs.getString("MaSach"));
                    jTextTenSach.setText(rs.getString("TenSach"));
                    jTextTheLoai.setText(rs.getString("TheLoai"));
                    jSpinnerSoTrang.setValue(rs.getInt("SoTrang"));
                    jTextTacGia.setText(rs.getString("TacGia"));
                    jTextNXB.setText(rs.getString("NXB"));
                    jTextViTri.setText(rs.getString("ViTri"));
                    jSpinnerSoLuong.setValue(rs.getInt("SoLuong"));
                }
                else{
                    JOptionPane.showMessageDialog(this, "Lỗi xác thực!");
                }
                ps.close();
                conn.close();
            }catch(HeadlessException | SQLException ex){
                System.out.println(ex);
            }
            jTextMaSach.disable();
        }
        else{
            jButtonChinhSua.hide();
        }
    }//GEN-LAST:event_jButtonChinhSuaActionPerformed

